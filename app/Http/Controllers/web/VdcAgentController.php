    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = Auth::user();
        if($user->isAdmin()){
            $validator = Validator::make($request->all(), VdcAgent::rules());
            if ($validator->fails()) {
                return Redirect::to('vdc/create')
                    ->withErrors($validator)
                    ->withInput($request->all());
            }

            VdcAgent::create($request->all());
            Session::flash('message', 'VDC successfully added!');
            return Redirect::route('vdcs.index');
        }else{
            return Redirect::to('logout');
        }
    }

